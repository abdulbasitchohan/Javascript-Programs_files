// This is String Type 

let Name = "Abdul Basit";
console.log("Value: " + Name + "," + " " + "|" + " " + "Type : " + " " + typeof Name);


// This is Number Type 
let Id = 111;
console.log("Value: " + Id + ", " + "| " + "Type : " + typeof Id);

// This is Boolean Type 
let Grade = true;
console.log("Value: " + Grade + ", " + "| " + "Type : " + typeof Grade);

// This is Null Type 
let CNIC = null;
console.log("Value: " + CNIC + ", " + "| " + "Type : " + typeof CNIC);

// This is Undefined Type 
let Domicile;
console.log("Value: " + Domicile + ", " + "| " + "Type : " + typeof Domicile);

// This is Bigint Type 
let B_Form = 12344645747646n;
console.log("Value: " + B_Form + ", " + "| " + "Type : " + typeof B_Form);


// This is Symbol Type 

let sign = Symbol("@");
console.log("Value: " , sign , ", " , "| " , "Type : " , typeof sign);





